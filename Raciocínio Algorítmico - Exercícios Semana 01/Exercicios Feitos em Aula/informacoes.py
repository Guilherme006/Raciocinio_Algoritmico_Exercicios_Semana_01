'''
    Crie um algoritmo que recebe quatro informações e armazena em variáveis:

    - Nome
    - CPF
    - Telefone
    - Ano de Nascimento

    Em seguida, imprima na tela esses valores.
'''

nome = str(input("Digite seu nome: "))
cpf = str(input("Digite seu CPF: "))
telefone = str(input("Digite seu nº de telefone: "))
ano_nascimento = int(input("Digite seu ano de nascimento: "))

print("Seu nome é:", nome)
print("Seu CPF é:", cpf)
print("Seu telefone é:", telefone)
print("Seu ano de nascimento é:", str(ano_nascimento))


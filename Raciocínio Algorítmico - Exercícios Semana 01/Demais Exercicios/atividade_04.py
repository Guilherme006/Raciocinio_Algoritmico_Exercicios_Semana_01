# Escrever um algoritmo para calcular a média de 4 notas.

nota_01 = float(input("Digite a sua primeira nota: "))
nota_02 = float(input("Digite a sua segunda nota: "))
nota_03 = float(input("Digite a sua terceira nota: "))
nota_04 = float(input("Digite a sua quarta nota: "))

media = (nota_01 + nota_02 + nota_03 + nota_04) / 4

print("A sua média é", str(media))
